// ani||csoc||longestNonDecreasingSubsequence

#include<stdio.h>
#include<set>

int n,ar[2000009];
std::multiset<int> m;

// for longest strictly increasing subsequence
// we use a std::set instead of std::multiset
// or instead of using upper_bound(), we use
// find() and increment the iterator by 1

int main()
{
    scanf("%d",&n);
    for(int i=0;i<n;i++)scanf("%d",ar+i);
    for(int i=0;i<n;i++)
    {
        std::multiset<int>::iterator it;
        m.insert(ar[i]);
        it=m.upper_bound(ar[i]);
        // for strictly increasing:
        // it=m.find(ar[i]);it++; 
        if(it!=m.end())
            m.erase(it);
    }
     printf("%lu\n",m.size());
     return 0;
}
